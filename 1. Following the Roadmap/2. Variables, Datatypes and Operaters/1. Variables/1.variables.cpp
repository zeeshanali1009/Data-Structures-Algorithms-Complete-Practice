#include <iostream>
using namespace std;
int main() {
    // Variable declarations and initializations
    int age = 21;                // Integer variable
    float weight = 68.5;         // Floating point variable
    char gender = 'M';           // Character variable
    bool isStudent = true;       // Boolean variable
    double salary = 50000.75;    // Double (more precision than float)

    // Displaying variable values
    cout << "Age: " << age << endl;
    cout << "Weight: " << weight << " kg" << endl;
    cout << "Gender: " << gender << endl;
    cout << "Is Student? " << isStudent << endl;
    cout << "Monthly Salary: $" << salary << endl;
    return 0;
}
